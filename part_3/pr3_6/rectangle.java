package part_3.pr3_6;
public class rectangle {
    int width = 4;
    int height = 5;
    public void getArea() {
        System.out.println("Area of Rectangle is " + (width * height));
    }
    public void getPerimeter() {
        System.out.println("Perimeter of Rectangle is " + (2 * (height + width)));
    }
}
